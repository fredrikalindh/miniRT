#include "miniRT.h"

double	ft_atof(char *str)
{
	double f;
	double div;

	f = 0.0;
	while (str && *str && *str != '.' && *str >= '0' && *str <= '9')
		f = f * 10 + *str++ - '0';
	div = 0.1;
	while (str && *(++str) && *str >= '0' && *str <= '9')
	{
		f += ((*str - '0') * div);
		div /= 10.0;
	}
	return (f);
}
